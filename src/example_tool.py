from tools.glossary_searcher import search_glossary
from llm.model_constructor import ModelConstructor
from langchain_core.messages import HumanMessage


# Создаем агента с тулом
agent = ModelConstructor.create_agent(
    model_name="openai/gpt-oss-120b:free",
    tools=[search_glossary],
    provider="openrouter",
    system_prompt="You are a legal assistant. Use search_glossary to find definitions of legal terms."
)

text="В соответствии с принципом подотчетности, оператор обязан \
    обеспечить соответствие обработки персональных данных требованиям \
        законодательства. Оператор должен назначить координатора по защите \
            данных или специалиста по защите данных, который будет отвечать за \
                внутренний контроль. Перед началом обработки необходимо провести оценку \
                    воздействия на защиту данных, особенно если планируется автоматизированное \
                        индивидуальное решение, например оценка кредитоспособности субъекта данных. \
                            Передача персональных данных в третьи страны допускается только при наличии \
                                адекватного решения Европейской комиссии или использовании таких инструментов, \
                                    как стандартные договорные клаузулы или обязательные корпоративные правила. Обработка \
                                        специальных категорий персональных данных, включая биометрические данные, данные о \
                                            состоянии здоровья, расовом или этническом происхождении, политических взглядах, \
                                                религиозных или философских убеждениях, а также данных о судимостях, допускается \
                                                    только с явно выраженного согласия субъекта данных или на основании прямого указания закона. В случае утечки данных оператор обязан уведомить уполномоченный орган о нарушении безопасности в течение 72 часов. Принцип минимизации данных требует, чтобы оператор собирал только те персональные данные, которые непосредственно необходимы для заявленной цели обработки. Европейский совет по защите данных и Европейский инспектор по защите данных осуществляют надзор за единообразным применением законодательства о защите данных на территории Европейского экономического пространства. Оператор обязан заключать письменное соглашение с процессором, которое определяет порядок обработки персональных данных и меры по обеспечению безопасности. Субъект данных имеет право на доступ к своим данным, их исправление, удаление, ограничение обработки, а также право на переносимость данных. В случае нарушения своих прав субъект данных может подать жалобу в уполномоченный орган. При проектировании информационных систем оператор должен руководствоваться принципами конфиденциальности по умолчанию и конфиденциальности при создании системы, чтобы обеспечить встроенную защиту персональных данных на всех этапах их обработки. Использование технологий, повышающих конфиденциальность, а также псевдонимизация и анонимизация данных рекомендуются в качестве эффективных мер снижения рисков для прав и свобод субъектов данных."

# Вызов для юридического термина
try:
    print('start')
    agent_response = agent.invoke({
        "messages": [HumanMessage(content=f"Find and translate legal terms in this text: {text}")]
    })
    thesaurus = agent_response['messages'][-1].content
    print(thesaurus)
except Exception as e:
    thesaurus = ""
    print('thesaurus is empty')
